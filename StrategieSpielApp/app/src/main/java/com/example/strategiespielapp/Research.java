package com.example.strategiespielapp;

public class Research {
    int industry;
    int laser;
    int logistics;
    int engine;
    int weapon;
    int shield;
    int armor;
    int account_id;

    public Research(int industry, int laser, int logistics, int engine, int weapon, int shield, int armor, int account_id) {
        this.industry = industry;
        this.laser = laser;
        this.logistics = logistics;
        this.engine = engine;
        this.weapon = weapon;
        this.shield = shield;
        this.armor = armor;
        this.account_id = account_id;
    }
}
